function startQuest() {

        alert("Добро пожаловать в безумное приключение с Дэдпулом в баре!");
    
        if (!confirm("Ты готов к незабываемой ночи?")) {
            alert("Ну и ладно, скучный ты!");
            return;
        }
    
        let position = 1; 
        let isDrunk = false;
    
        while (true) {
            let choice;
    
            switch (position) {
                case 1:
                    choice = prompt("Ты сидишь в баре с Дэдпулом, когда вдруг врываются вооруженные головорезы! \n1 – Достать пистолет из-за пазухи \n2 – Спрятаться за стойкой \n3 – Заказать еще выпить");
    
                    if (choice === null) return alert("Ты остался сидеть за барной стойкой. Смерть от шальной пули.");
                    if (choice === "1") {
                        alert("Блин, ты же забыл его взять! Головорезам не понравилась твоя выходка. Смерть от выстрела в голову.");
                        position = 1;
                    } else if (choice === "2") {
                        alert("Ты прячешься за стойкой. Дэдпул орёт: 'Какого чёрта!'");
                        position = 2;
                    } else if (choice === "3") {
                        alert("Ты заказываешь ещё выпить. Дэдпул относится с пониманием. Теперь ты пьян.");
                        isDrunk = true;
                        position = 3;
                    } else {
                        alert("Выбери 1, 2 или 3, балбес!");
                    }
                    break;
    
                case 2:
                    choice = prompt("Ты спрятался за стойкой. Что делать дальше? \n1 – Взять бутылку и кинуть в врага \n2 – Позвать Дэдпула на помощь \n3 – Вылезти и сказать 'Я сдаюсь!'");

                    if (choice === null) return alert("Ты остался сидеть за барной стойкой. Смерть от шальной пули.");
                    if (choice === "1") {
                        alert("Пока ты копошишься в поисках подходящей бутылки, тебя находят за стойкой и убивают. Какого?? Не мог побыстрее найти? ... Смерть из-за медлительности.");
                        position = 1;
                    } else if (choice === "2") {
                        alert("Сам разберёшься! ... Ладно, шучу. Дедпул начинает перестрелку первым.");
                        position = 4;
                    } else if (choice === "3") {
                        alert("Ты выходишь с поднятыми руками. Бандиты переглядываются, а потом убивают тебя. Ну, бывает. Смерть из-за глупости.");
                        position = 1;
                    } else {
                        alert("Выбирай нормально!");
                    }
                    break;
    
                case 3:
                    choice = prompt("Ты пьян, и всё плывёт. Что делать? \n1 – Попытаться сбежать \n2 – Вызвать такси и позвать Дэдпула с собой \n3 – Спеть песню");
    
                    if (choice === null) return alert("Ты уснул за барной стойкой, и на этом история заканчивается.");
                    if (choice === "1") {
                        alert("Ты пытаешься сбежать, но путаешься в собственных ногах и падаешь. Смерть от удара о барную стойку. Серьёзно? Настолько банально?!");
                        position = 1;
                    } else if (choice === "2") {
                        alert("Ты вызываешь такси, но Дэдпул требует сначала разобраться с бандитами. Блин, надо закончить пока не начнется платное ожидание...");
                        position = 4;
                    } else if (choice === "3") {
                        alert("Ты начинаешь орать песню. Все замирают. Даже враги. Ты выигрываешь время и Дедпул разибрается с проблемой!");
                        return;
                    } else {
                        alert("Ну выбери что-то нормальное уже!");
                    }
                    break;
    
                case 4:
                    choice = prompt("Перестрелка в разгаре! Что делать? \n1 – Прикрывать Дэдпула \n2 – Ворваться в толпу с криком \n3 – Подобрать оружие с пола");
    
                    if (choice === null) return alert("Ты замешкался, и один из головорезов выстрелил в тебя первым.");
                    if (choice === "1") {
                        alert("Ты прикрываешь Дэдпула, и он успевает разобраться со всеми. Бар и твоя жо... жизнь спасены!");
                        return;
                    } else if (choice === "2") {
                        if (isDrunk) {
                            alert("Ты бежишь вперёд и... каким-то образом всех побеждаешь. Пьяная удача!");
                            return;
                        } else {
                            alert("Ты врываешься в толпу и сразу получаешь пулю. Ошибка! Фатальная.");
                            position = 1;
                        }
                    } else if (choice === "3") {
                        alert("Ты хватаешь оружие и... оно сломано! Правда думал, что на полу валяются хорошие пушки? Думать нужно. Смерть от пули.");
                        position = 1;
                    } else {
                        alert("Выбери 1, 2 или 3, а не фигню какую-то.");
                    }
                    break;
            }
        }
    }
    